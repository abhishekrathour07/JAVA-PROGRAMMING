//  java program to find the sum of first 10 natural numbers
    
public class ch14_online5 {
    public static void main(String[] args) {
        System.out.println(" Sum of first 10 natural numbers are :");
        int sum =0;
        for(int i=1;i<11;i++){
            sum =sum+i;
        }
        System.out.println(sum);
    }
}
